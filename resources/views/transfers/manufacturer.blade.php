                                        <b style="color: #716aca">Heat Transfer Production Process:</b><br>
                                        <ol style="margin-left: 5px;">
                                            <li>A PET film of 105% the size of the original artwork is taken.</li>
                                            <li>the release agent is applied evenly on the PET film</li>
                                            <li>
                                                Base colors are used to hand mix the artwork’s Pantone colors.
                                                If a photo is to be printed, the pre-mixed colors Cyan, Magenta, Yellow and Black are prepared.
                                            </li>
                                            <li>
                                                Screens are built, a screen-makers ink is applied and the designs
                                                are burnt into the screens. Each color requires a separate screen.
                                            </li>
                                            <li>
                                                The screen is showered to wash out the none-burnt screen-makers ink.
                                                This way the screen only lets the ink through open mesh squares.
                                            </li>
                                            <li>
                                                The screens are placed one after another on the PET film with the
                                                dried release agent and the pre-mixed colors are printed on.
                                            </li>
                                            <li>
                                                Like your basic printer’s inks, heat transfer inks look best when
                                                printed on white backgrounds. Therefor a white carrier is added as
                                                final layer, after all colors have been printed.
                                            </li>
                                            <li>
                                                If your design has no transparencies, the white layer can be printed
                                                over the complete transfer paper and does not require a separate screen.
                                                However if your design has transparent parts, a separate screen must be
                                                prepared to print the white background around the transparent spots of your design.
                                                Since transparencies require us to make an extra film for the white background,
                                                we call this screen “the transparency screen”.
