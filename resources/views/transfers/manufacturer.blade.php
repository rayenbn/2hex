                                        <b style="color: #716aca">Transfer Printing Process:</b><br>
                                        To transfer print heat transfers to skateboard decks, longboard decks, cruiser
                                        decks and skimboards a heat transfer press is required. The heat transfer press
                                        consists of two large silicon wheels which under heat and pressure enable
                                        the ink to be released from the carrier and penetrate the wood’s structure
                                        to achieve a strongly fixed print.
                                        <br><br>

                                        <b style="color: #716aca">Heat Transfer Machines:</b><br>
                                        Heat transfer presses built in Germany or USA are at around 20.000 USD.
                                        High quality Chinese heat transfer presses are sold at half that price.
                                        Lower quality machines are sold even cheaper. Due to our mass production quantities,
                                        2HEX currently offers the best price for high quality heat transfer presses at 6000 USD.
                                        However: Chinese heat transfer presses do not meet US’ or European safety
                                        standards and must therefor be retrofitted by the buyer to ensure a safe production.
